//! Los principios son pautas o caracteristicas que un lenguaje debe cumplir para que pueda ser considerado orientado a objetos

//! El principal proposito de la POO, es crear código que sea modular y reutilizable

//! La poo tiene 2 requirimientos para considerarlo como tal:
/*! 1. Capacidad de modelar problemas:
        -Association: Objetos con la capacidad de referir a otros objetos, poder unir dos objetos y enlazarlos

        -Aggregation: La capacidad de un objeto de referir a otros objetos independientes

        -Composition: La capacidad de un objeto de referir a otros objetos dependientes.

    2. Garantizar la modularidad y reusabilidad de codigo:
        -Encapsulation: La capacidad de un objeto de concentrar datos y codigo en una sola entidad ocultando sus detalles internos.

        -Inheritance: Es el mecanismo por el cual un objeto puede adquirir algunas o todas las caracteristicas de otro o mas objetos.

        -Polymorphism: Es la capacidad de procesar objetos con distintos tipos de datos y distintas estructuras, pero que alfinal pueden devolvernos una respuesta
*/

//! EL proposito de la POO es modelar a través de objetos
//! Cuando la usamos vamos a estar creando codigo, vamos a estar modelando a traves de estas entidades para poder simplificar nuestra realidad

//! A esta simplificacion de la realidad se le conoce con la palabra "abstraccion"